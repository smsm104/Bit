package com.bittago.repository.vo;

public class Bus {

	private int busNo;
	private String busLicense;
	private String busGrade;
	private int maxSeatCnt;
	public int getBusNo() {
		return busNo;
	}
	public void setBusNo(int busNo) {
		this.busNo = busNo;
	}
	public String getBusLicense() {
		return busLicense;
	}
	public void setBusLicense(String busLicense) {
		this.busLicense = busLicense;
	}
	public String getBusGrade() {
		return busGrade;
	}
	public void setBusGrade(String busGrade) {
		this.busGrade = busGrade;
	}
	public int getMaxSeatCnt() {
		return maxSeatCnt;
	}
	public void setMaxSeatCnt(int maxSeatCnt) {
		this.maxSeatCnt = maxSeatCnt;
	}
	
	
}
